#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] && s[i] != (unsigned char)c)
		i++;
	if ((unsigned char)c != '\0' && s[i] == '\0')
		return (NULL);
	return ((char *)(s + i));
}
